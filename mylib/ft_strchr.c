char	*ft_strchr(char *s, int c)
{
	int		count1;

	count1 = 0;
	while (s[count1] != '\0')
	{
		if ((int)s[count1] == c)
		{
			return ((char *)s + count1);
		}
		count1++;
	}
	return (0);
}